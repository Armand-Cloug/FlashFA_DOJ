            <div id="b" class="card"> ⚖️ Présentation du Bureau des Avocats Pénalistes (BAP)
                <ul> 
                    Le Bureau des Avocats Pénalistes (BAP) est une entité clé du système judiciaire, dédiée à la défense des citoyens confrontés à la justice pénale. Son rôle principal est d’assurer une représentation légale lors des comparutions immédiates, 
                    garantissant ainsi l'accès à un avocat pour toute personne en ayant besoin.
                    <br><br>
                    Les avocats du BAP interviennent principalement lorsque la désignation d’un avocat d’office est requise. Ils assurent une défense initiale en audience, mais ne plaident pas lors des jugements, sauf en cas d’indisponibilité des 
                    cabinets privés. En revanche, lorsque ces derniers reprennent un dossier traité par le BAP, une collaboration étroite est mise en place pour assurer une transition fluide et préserver la qualité de la défense.
                    <br><br>
                    <strong> 📌 Rôle et Fonctions du Bureau des Avocats Pénalistes </strong>
                    <ul> 🔹 Assistance en comparution immédiate : Les avocats du BAP représentent les accusés lors de leurs premières audiences devant le tribunal. Ils examinent le dossier, conseillent leur client et assurent leur défense. </ul>
                    <ul> 🔹 Garantir une défense équitable : Le BAP s’engage à représenter toute personne nécessitant un avocat d’office, indépendamment de sa situation financière ou de la gravité des charges retenues contre elle. </ul>
                    <ul> 🔹 Collaboration avec les cabinets privés : Si un accusé choisit d’être défendu par un avocat privé pour la suite de son affaire, le BAP transmet l’ensemble des informations pertinentes au cabinet concerné afin d’assurer une continuité optimale de la défense. </ul>
                    <ul> 🔹 Respect de l’éthique et des droits fondamentaux : Tout comme les procureurs, les avocats du BAP sont tenus par une obligation de probité et d’équité, veillant à ce que leurs clients bénéficient d’un procès juste et impartial. </ul>
                    <br>
                    <strong> 🏛 Structure et Organisation </strong> <br>
                    Le Bureau des Avocats Pénalistes est composé de plusieurs membres qui jouent un rôle essentiel dans son fonctionnement :
                    <ul> 🔹 Apprenants : Nouveaux arrivants en formation, assistant à des comparutions sous supervision. </ul>
                    <ul> 🔹 Secrétaires : Chargés de la gestion administrative et du suivi des dossiers. </ul>
                    <ul> 🔹 Avocats Pénalistes : Responsables de la défense des accusés en comparution immédiate, ils interviennent directement devant la cour. </ul>
                    <br>
                    <strong> 🎓 Formation et Accès au BAP </strong> <br>
                    L’intégration au Bureau des Avocats Pénalistes ne requiert aucune expérience préalable en droit. Cependant, une formation obligatoire de deux semaines est nécessaire avant de pouvoir exercer :
                    <ul> 1️⃣ Observation et participation aux comparutions sous la supervision d’un procureur. </ul>
                    <ul> 2️⃣ Encadrement possible par des avocats privés pour affiner les compétences. </ul>
                    <ul> 3️⃣ Passage du barreau après la formation. </ul>
                    <ul> 4️⃣ Obtention du statut d’Avocat Pénaliste et début de l’exercice au BAP. </ul>
                    <br>
                    <strong> 🚀 Évolutions et Perspectives </strong> <br>
                    Après un minimum d’un mois d’expérience, les avocats du BAP peuvent évoluer vers d’autres opportunités, telles que :
                    <ul> ✔️ Intégration d’un cabinet privé. </ul>
                    <ul> ✔️ Création de leur propre cabinet. </ul>
                    <ul> ✔️ Spécialisation en Fiscalité ou Administration. </ul>
                    <ul> ✔️ Accès à des fonctions judiciaires, notamment en tant que juge. </ul>
                    <br>
                    <strong> 💼 Conditions et Rémunération </strong>
                    <ul> 🔹 Apprenants : 10.000$ par semaine. </ul>
                    <ul> 🔹 Avocats Pénalistes : 15.000$ par semaine. </ul>
                    <ul> 🔹 Prime de 5.000$ en fonction du nombre de comparutions effectuées. </ul>
                    <ul> 🔹 Présence minimale requise : 5 soirs par semaine. </ul>
                    <br>
                    <strong> 📩 Rejoindre le Bureau des Avocats Pénalistes </strong> <br>
                    Le BAP joue un rôle essentiel dans l’équilibre du système judiciaire en garantissant à chaque individu un accès à la défense. Vous souhaitez vous engager pour la justice et défendre les droits fondamentaux ? Rejoignez le Bureau des Avocats 
                    Pénalistes dès aujourd’hui et prenez part à une mission essentielle au sein du DOJ.
