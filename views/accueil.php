                    Les U.S. Marshals ont des missions variées et sont spécialisés dans l’arrestation des fugitifs, la protection des témoins et des juges, la gestion des détenus fédéraux, ainsi que la sécurité des tribunaux.
                    <br><br>
                    <strong>⚔️ Missions Principales des U.S. Marshals </strong>
                    <br><br>
                    <ul> <strong>1️⃣ Arrestation des Fugitifs et Opérations de Recherche 🚔 </strong>
                        <ul> Les U.S. Marshals sont les meilleurs chasseurs de fugitifs du pays. </ul>
                        <ul> Ils traquent et arrêtent les criminels recherchés par la justice fédérale, notamment les trafiquants de drogue, les terroristes et les criminels violents. </ul>
                        <ul> Ils collaborent avec les polices locales et federal ainqi qu'avec d’autres organismes pour capturer les fugitifs dangereux. </ul></ul>
                    <br>
                    <ul> <strong>2️⃣ Programme de Protection des Témoins (WITSEC) 🕵️‍♂️ </strong>
                        <ul> Ils gèrent le programme fédéral de protection des témoins (Witness Security Program - WITSEC).</ul>
                        <ul> Ils assurent la relocation et l’anonymisation des témoins-clés dans les affaires criminelles majeures.</ul>
                        <ul> Ils garantissent la sécurité des témoins et de leurs familles, souvent contre des cartels, la mafia ou des organisations terroristes.</ul></ul>
                    <br>
                    <ul> <strong>3️⃣ Sécurité des Tribunaux Fédéraux et des Juges 🏛️ </strong>
                        <ul> Ils protègent les tribunaux fédéraux, les juges, les procureurs et les avocats contre toute menace.</ul>
                        <ul> Ils sécurisent les procès à haut risque, notamment ceux impliquant des criminels notoires ou du crime organisé.</ul></ul>
                    <br>
                    <ul> <strong>4️⃣ Gestion des Détenus Fédéraux et des Transferts 🚨 </strong>
                        <ul> Ils sont responsables, en collaboration avec le SASP et le SOB, de la sécurisation des prisonniers dangereux avant leur procès.</ul></ul>
                    <br>
                    <strong>🎖️ Pourquoi sont-ils Essentiels ? </strong>
                    <ul> Les U.S. Marshals sont des forces d’élite spécialisées, capables d’intervenir sur des missions à haut risque.</ul>
                    <ul> Ils jouent un rôle critique dans la sécurité du système judiciaire, l’arrestation des criminels dangereux et la protection des témoins menacés.</ul>
