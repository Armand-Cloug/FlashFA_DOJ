                <ul> 
                    Le United States Marshals Service (USMS) est la plus ancienne agence fédérale des forces de l’ordre aux États-Unis, fondée en 1789. Il est rattaché au Département de la Justice et agit en tant que bras armé du système judiciaire fédéral.
